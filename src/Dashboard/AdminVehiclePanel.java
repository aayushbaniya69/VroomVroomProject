    private void loadVehicleList(java.util.List<Vehicle> vehicles) {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // clear old data

    for (Vehicle v : vehicles) {
        model.addRow(new Object[]{
            v.getName(),
            v.getType(),
            v.getPricePerDay(),
            v.getStatus(),
            v.getImagePath()
        });
    }
}


